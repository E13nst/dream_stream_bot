package com.example.dream_stream_bot.service.access;

/** Причина решения {@link AccessGate}. */
public enum AccessReason {

    /** Подписка активна (триал/оплата) — доступ разрешён. */
    ACTIVE,

    /** Сообщение в группе не адресовано боту (нет mention/reply/trigger) — молча игнорируем. */
    GROUP_TRIGGER_NOT_MATCHED,

    /** Сообщение не из поддерживаемого типа чата (канал и т.п.). */
    UNSUPPORTED_CHAT,

    /** Подписки нет — нужно онбординг или /start. */
    NO_SUBSCRIPTION,

    /** Подписка создана, но онбординг не пройден (согласия не приняты). */
    PENDING_CONSENT,

    /** Подписка истекла. */
    EXPIRED,

    /** Подписка отменена. */
    CANCELLED,

    /** Заблокирована из-за непринятой материальной правки согласий. */
    BLOCKED_CONSENT,

    /** В грейс-периоде после публикации новой версии — отвечаем, но напоминаем раз в сутки. */
    CONSENT_GRACE,

    /** Лимит участников группы превышен мягко (≤30%) — отвечаем, но напоминаем владельцу. */
    PARTICIPANT_LIMIT_SOFT,

    /** Лимит участников превышен жёстко (>30%) — этому участнику не отвечаем. */
    PARTICIPANT_LIMIT_HARD
}
