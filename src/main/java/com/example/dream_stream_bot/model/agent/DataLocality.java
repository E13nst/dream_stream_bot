package com.example.dream_stream_bot.model.agent;

/**
 * Локализация обработки данных агента.
 * Управляет тем, какие согласия запрашивать у пользователя при онбординге
 * и какие compliance-меры применять.
 */
public enum DataLocality {
    /**
     * Обработка запросов выполняется только на территории Российской Федерации.
     * Трансграничной передачи нет — отдельное согласие на трансграничную передачу
     * запрашивать не нужно.
     */
    LOCAL_RU,

    /**
     * Запросы передаются иностранному LLM-поставщику (напрямую или через российский
     * шлюз, фактически передающий данные за рубеж). Требуется отдельное письменное
     * согласие субъекта по ст. 12 152-ФЗ.
     */
    CROSS_BORDER
}
